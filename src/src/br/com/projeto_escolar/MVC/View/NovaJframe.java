package br.com.projeto_escolar.MVC.View;

import javax.swing.*;

public class NovaJframe {
    public static void main(String[] args) {
        JFrame frame = new JFrame();

        frame.setTitle("Sistema escolar"); // dá titulo ao frame
        frame.setSize(400,400); // determina a largura e altura
        frame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE); // permite fechar ao clicar no botão de fechar
        frame.setVisible(true); // torna visivel o frame



    }
}
